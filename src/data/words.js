// Get a random word based on difficulty
export function getRandomWord(difficulty = 'mixed') {
    let wordList

    if (difficulty === 'easy') {
        wordList = EASY_WORDS
    } else if (difficulty === 'medium') {
        wordList = MEDIUM_WORDS
    } else if (difficulty === 'hard') {
        wordList = HARD_WORDS
    } else {
        // Mixed: weighted random selection
        const rand = Math.random()
        if (rand < 0.4) {
            wordList = EASY_WORDS
        } else if (rand < 0.75) {
            wordList = MEDIUM_WORDS
        } else {
            wordList = HARD_WORDS
        }
    }

    return wordList[Math.floor(Math.random() * wordList.length)]
}

export function getRandomBossPhrase() {
    return BOSS_PHRASES[Math.floor(Math.random() * BOSS_PHRASES.length)]
}
